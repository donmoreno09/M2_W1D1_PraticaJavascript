/* ESERCIZIO 1
 Elenca e descrivi i principali datatype in JavaScript. Prova a spiegarli come se volessi farli comprendere a un bambino.
*/

/* In JavaScript ci sono diversi tipi di dati (datatypes):

String: È come un insieme di lettere, parole o frasi. Per esempio, il tuo nome o un messaggio. Si scrive tra virgolette, tipo "Ciao!".

Number: Sono tutti i numeri, sia interi come 7, che decimali come 3.14. Con i numeri possiamo fare addizioni, sottrazioni e così via.

Boolean: È un dato che può essere solo vero o falso. Per esempio, true o false, usato per fare decisioni nel codice.

Undefined: Questo tipo di dato appare quando una variabile non ha ancora un valore assegnato. Credo sia come una scatola vuota.

Null: È come dire che una variabile ha proprio "nulla" al suo interno. Una scatola che esiste, ma che non contiene nulla o che contiene nessun valore.

Object: È una specie di scatola che può contenere tanti valori di tipi diversi, organizzati come coppie chiave-valore. Per esempio, una persona può avere un nome, un’età, l'indirizzo e così via.

Array: È una lista di valori. Può essere una lista di nomi, numeri, o qualsiasi altra cosa. Si scrive tra parentesi quadre, tipo [1, 2, 3]. */

